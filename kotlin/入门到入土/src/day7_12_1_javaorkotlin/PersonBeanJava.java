package day7_12_1_javaorkotlin;

public class PersonBeanJava {
    private String name;
    private int age;
    private String sex;
    private int score;

    public String getName() {
        return name;
    }

    public int getAge() {
        return age;
    }

    public String getSex() {
        return sex;
    }

    public int getScore() {
        return score;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public void setSex(String sex) {
        this.sex = sex;
    }

    public void setScore(int score) {
        this.score = score;
    }
}
